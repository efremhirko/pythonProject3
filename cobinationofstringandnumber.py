# String Formatting:
#
# The code uses an f-string, which is a concise way to embed expressions within strings in Python.
# It starts with an f before the opening quotation mark, indicating an f-string.
# The curly braces {} are placeholders for values to be inserted into the string.
print(f"Joe Warren is {str(52)} years old.")
print("\nstudent name: Ephrem Hirko\n")
print("ID: RU1360/12")